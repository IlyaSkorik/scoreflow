/// Доля длительности от целой ноты (для контроля заполнения такта).
const Map<String, double> durationFraction = {
  'w': 1.0,
  'h': 0.5,
  'q': 0.25,
  '8': 0.125,
  '16': 0.0625,
  '32': 0.03125,
  '64': 0.015625,
};

/// Доля длительности с учётом точек. Каждая точка добавляет половину
/// предыдущего приращения: 1 точка = base·1.5, 2 точки = base·1.75 и т.д.
/// Расширяемо под двойной/тройной пунктир без изменения остального кода.
double noteFraction(String duration, int dots) {
  final base = durationFraction[duration] ?? 0;
  var add = base / 2;
  var total = base;
  for (var i = 0; i < dots; i++) {
    total += add;
    add /= 2;
  }
  return total;
}

/// Высотный «ранг» ключа VexFlow — для сортировки голов аккорда снизу вверх
/// (VexFlow ждёт упорядоченные головки). Понимает букву a–g с диезами/бемолями
/// и октаву; головку (3-й сегмент, напр. "/x2" у тарелок) игнорирует — у
/// ударных порядок идёт по линии стана.
int keyPitchRank(String key) {
  const semi = {'c': 0, 'd': 2, 'e': 4, 'f': 5, 'g': 7, 'a': 9, 'b': 11};
  final parts = key.split('/');
  final la = parts[0].toLowerCase();
  var s = semi[la.isNotEmpty ? la[0] : 'c'] ?? 0;
  for (var k = 1; k < la.length; k++) {
    if (la[k] == '#') s += 1;
    if (la[k] == 'b') s -= 1;
  }
  final octave = parts.length > 1 ? (int.tryParse(parts[1]) ?? 4) : 4;
  return octave * 12 + s;
}

