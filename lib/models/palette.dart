/// Доля длительности от целой ноты (для контроля заполнения такта).
const Map<String, double> durationFraction = {
  'w': 1.0,
  'h': 0.5,
  'q': 0.25,
  '8': 0.125,
  '16': 0.0625,
  '32': 0.03125,
  '64': 0.015625,
};

/// Доля длительности с учётом точек. Каждая точка добавляет половину
/// предыдущего приращения: 1 точка = base·1.5, 2 точки = base·1.75 и т.д.
/// Расширяемо под двойной/тройной пунктир без изменения остального кода.
double noteFraction(String duration, int dots) {
  final base = durationFraction[duration] ?? 0;
  var add = base / 2;
  var total = base;
  for (var i = 0; i < dots; i++) {
    total += add;
    add /= 2;
  }
  return total;
}

