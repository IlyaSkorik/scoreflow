// [ScoreFlow engine] Velocity -> gain — ЕДИНСТВЕННАЯ кривая преобразования
// громкости. Velocity (0..~1.1) разрешается ОДИН раз в playback-компиляторе
// (domain/dynamics.js); здесь — единственное место, где она превращается в
// Web Audio gain, общее для сэмплов и синтеза, рояля и ударных. Так у playback
// один источник правды и по громкости тоже: ни один движок не считает кривую
// сам.
//
// Перцептивная форма: степенная (EXP>1) расширяет тихий диапазон, чтобы
// pp/mf/ff РАЗЛИЧАЛИСЬ ЯВНО, а не сливались. FLOOR держит самые тихие оттенки
// слышимыми (ppp != тишина). Значение для v=1.0 (ff) == [peak]; v=1.1 (fff)
// даёт ~1.13·peak — поэтому fff ГРОМЧЕ ff (вызывающий клампит финальный gain).
//
// ВАЖНО (sampled): выбор velocity-СЛОЯ сэмпла остаётся на целочисленной
// velocity 1..127 (сэмплы записаны до ff), а ГРОМКОСТЬ берётся отсюда из СЫРОЙ
// float-velocity — иначе ff и fff (обе -> 127) звучали бы одинаково.
export const VELOCITY_FLOOR = 0.08; // доля громкости при velocity≈0 (слышимость ppp)
export const VELOCITY_EXP = 1.4;    // >1 — расширение тихого диапазона

// Громкость для [velocity] (0..~1.1) и пикового уровня [peak] (уровень ff).
// Без верхнего клампа: fff осознанно превышает peak; клампит вызывающий под
// свой потолок во избежание клиппинга при полифонии.
export function velocityGain(velocity, peak) {
    const v = velocity == null ? 0.75 : Math.max(0, velocity);
    const p = peak == null ? 1.0 : peak;
    return p * (VELOCITY_FLOOR + (1 - VELOCITY_FLOOR) * Math.pow(v, VELOCITY_EXP));
}
